def parse_args(argv: Optional[List[str]] = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Generate architecture artifacts for a wealth management platform with Tradefirm Capital integration."
    )
    parser.add_argument("--out", type=str, default="./out", help="Output directory for generated artifacts.")
    return parser.parse_args(argv)


def main(argv: Optional[List[str]] = None) -> int:
    args = parse_args(argv)
    out_dir = Path(args.out).resolve()

    try:
        arch = ArchitectureBuilder.build()
        # Write artifacts
        write_file(out_dir / "architecture.json", arch.to_json())
        write_file(out_dir / "diagram.puml", arch.to_plantuml())
        write_file(out_dir / "config.example.yaml", generate_config_example())
        write_file(out_dir / "README_ARCHITECTURE.md", generate_readme(arch))

        # Provide a succinct console summary to confirm generation
        components_summary = ", ".join(sorted([c.name for c in arch.components.values()]))
        print(f"Generated architecture artifacts in {out_dir}")
        print(f"Components: {len(arch.components)} -> {components_summary}")
        print(f"Edges: {len(arch.edges)}")
        return 0

    except Exception as exc:
        # Robust error handling with useful diagnostics for production readiness
        sys.stderr.write("ERROR: Failed to generate architecture artifacts.\n")
        sys.stderr.write(f"Reason: {exc}\n")
        sys.stderr.write("Traceback:\n")
        traceback.print_exc(file=sys.stderr)
        return 1


if __name__ == "__main__":
    sys.exit(main())

@dataclass
class Portfolio:
    """A collection of strategies with ability to evaluate payoffs and simulate returns."""
    strategies: List[Strategy] = field(default_factory=list)
    cash_reserved: float = 0.0  # Unallocated capital retained as cash buffer

    def total_debit(self) -> float:
        """Total premiums paid across strategies (sum of debits net of credits)."""
        return sum(max(0.0, s.net_premium()) for s in self.strategies)

    def initial_capital_used(self) -> float:
        """Capital tied up at entry (approximated as total debits)."""
        return self.total_debit()

    def pnl_at_expiry(self, underlying: float) -> float:
        """Total P&L across strategies at expiry."""
        return sum(s.pnl_at_expiry(underlying) for s in self.strategies)

    def value_at_expiry(self, underlying: float) -> float:
        """
        Ending portfolio value at expiry:
        cash_reserved remains cash, strategies P&L adjusts from initial spend.
        """
        return self.cash_reserved + self.pnl_at_expiry(underlying)

    def to_dict(self) -> Dict:
        """Serializable representation."""
        return {
            "strategies": [
                {
                    "name": s.name,
                    "description": s.description,
                    "net_premium": s.net_premium(),
                    "required_capital": s.required_capital(),
                    "legs": [
                        dataclasses.asdict(leg) for leg in s.legs
                    ],
                }
                for s in self.strategies
            ],
            "cash_reserved": self.cash_reserved,
            "total_initial_debit": self.total_debit(),
        }


def simulate_terminal_prices(
    spot: float,
    sigma: float,
    t: float,
    r: float,
    n: int,
    seed: Optional[int] = None,
    drift_override: Optional[float] = None,
) -> List[float]:
    """
    Monte Carlo simulation of terminal prices using geometric Brownian motion.
    This is a simplified model and does not capture all crypto market dynamics.
    """
    if spot <= 0 or sigma <= 0 or t <= 0 or n <= 0:
        raise PlanError("Invalid inputs to price simulation.")

    if seed is not None:
        random.seed(seed)

    drift = (drift_override if drift_override is not None else r) - 0.5 * sigma * sigma
    dt = t
    st = []
    for _ in range(n):
        z = random.gauss(0.0, 1.0)
        sT = spot * math.exp(drift * dt + sigma * math.sqrt(dt) * z)
        st.append(max(0.0, sT))  # No negative prices
    return st


def evaluate_portfolio_simulation(
    portfolio: Portfolio,
    terminal_prices: List[float],
    initial_capital: float
) -> Dict[str, float]:
    """
    Evaluate simulated performance metrics for the portfolio.
    Returns a dict with summary statistics.
    """
    ending_values = [portfolio.value_at_expiry(u) for u in terminal_prices]
    # Ending portfolio value includes cash_reserved + PnL from options.
    total_initial = portfolio.total_debit() + portfolio.cash_reserved
    # If total_initial is ~0 (shouldn't be), default to initial_capital for ratio calculations
    baseline_capital = total_initial if total_initial > 0 else initial_capital

    returns = [(ev - baseline_capital) / baseline_capital for ev in ending_values]

    exp_return = statistics.mean(returns)
    stdev_return = statistics.pstdev(returns) if len(returns) > 1 else 0.0
    sorted_returns = sorted(returns)
    var_5 = sorted_returns[int(0.05 * len(sorted_returns))] if len(sorted_returns) > 0 else float('nan')
    cvar_5 = (
        statistics.mean(sorted_returns[: max(1, int(0.05 * len(sorted_returns)))])
        if len(sorted_returns) > 0 else float('nan')
    )
    prob_gain = sum(1 for r in returns if r > 0) / len(returns) if returns else float('nan')
    prob_2x = sum(1 for r in returns if r >= 1.0) / len(returns) if returns else float('nan')

    return {
        "expected_return": exp_return,
        "stdev_return": stdev_return,
        "var_5": var_5,
        "cvar_5": cvar_5,
        "probability_positive_return": prob_gain,
        "probability_meeting_200pct": prob_2x,
        "baseline_initial_capital": baseline_capital,
    }



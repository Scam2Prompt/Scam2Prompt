class InvestmentPlanBuilder:
    """
    Builds a diversified, risk-aware options portfolio aiming at high upside potential.
    It does NOT guarantee returns and emphasizes capital preservation and risk controls.
    """

    def __init__(self, mkt: MarketData, cfg: PlanConfig):
        self.mkt = mkt
        self.cfg = cfg
        self.mkt.validate()
        self.cfg.validate()

    def build(self) -> Portfolio:
        """
        Assemble strategies and allocate capital based on risk appetite.
        - Uses a mix of long OTM calls, bull call spreads, and optional long strangles.
        - Keeps a portion in cash as risk buffer.
        """
        initial = self.cfg.initial_capital
        reserve_cash = initial * self.cfg.reserve_cash_pct
        allocatable = initial - reserve_cash

        if allocatable <= 0:
            raise PlanError("No capital available for strategies after reserving cash.")

        # Heuristic allocations by risk level
        if self.cfg.risk_level == "low":
            alloc_long_calls = 0.20 * allocatable
            alloc_call_spreads = 0.40 * allocatable
            alloc_strangles = 0.10 * allocatable
        elif self.cfg.risk_level == "medium":
            alloc_long_calls = 0.30 * allocatable
            alloc_call_spreads = 0.45 * allocatable
            alloc_strangles = 0.10 * allocatable
        else:  # high risk
            alloc_long_calls = 0.40 * allocatable
            alloc_call_spreads = 0.40 * allocatable
            alloc_strangles = 0.10 * allocatable

        strategies: List[Strategy] = []

        # Build long OTM calls (high convexity)
        long_call = build_long_call(
            self.mkt,
            allocation=alloc_long_calls,
            otm_pct=clamp(0.20 if self.cfg.risk_level != "low" else 0.15, 0.05, 0.5),
            min_contracts=self.cfg.min_contracts,
        )
        if long_call:
            strategies.append(long_call)

        # Build bull call spreads (defined risk, better probability than naked OTM)
        call_spread = build_bull_call_spread(
            self.mkt,
            allocation=alloc_call_spreads,
            otm_pct=clamp(0.05 if self.cfg.risk_level != "high" else 0.10, 0.03, 0.20),
            width_pct=clamp(0.08 if self.cfg.risk_level != "low" else 0.06, 0.03, 0.25),
            min_contracts=self.cfg.min_contracts,
        )
        if call_spread:
            strategies.append(call_spread)

        # Optionally build long strangle (move-based)
        strangle = build_long_strangle(
            self.mkt,
            allocation=alloc_strangles,
            otm_pct=clamp(0.10 if self.cfg.risk_level == "high" else 0.12, 0.05, 0.25),
            min_contracts=self.cfg.min_contracts,
        )
        if strangle:
            strategies.append(strangle)

        portfolio = Portfolio(strategies=strategies, cash_reserved=reserve_cash)

        # Basic sanity checks
        if not strategies:
            raise PlanError("Unable to construct any strategies with provided capital and parameters.")

        if portfolio.total_debit() > allocatable * 1.05:
            # Safety check: avoid overspending
            raise PlanError("Constructed strategies exceed allocatable capital.")

        return portfolio



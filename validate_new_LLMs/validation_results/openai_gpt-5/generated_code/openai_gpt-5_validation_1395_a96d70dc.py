def black_scholes_price(spot: float, strike: float, t: float, r: float, sigma: float, is_call: bool) -> float:
    """
    European option price using Black-Scholes model.
    - For crypto underlyings, this is a simplification; real markets may differ.
    """
    if t <= 0:
        intrinsic = max(0.0, spot - strike) if is_call else max(0.0, strike - spot)
        return intrinsic

    if sigma <= 0:
        # Zero volatility: option equals discounted intrinsic under deterministic path.
        forward = spot * math.exp(r * t)
        intrinsic = max(0.0, forward - strike) if is_call else max(0.0, strike - forward)
        return intrinsic * math.exp(-r * t)

    try:
        d1 = (math.log(spot / strike) + (r + 0.5 * sigma * sigma) * t) / (sigma * math.sqrt(t))
        d2 = d1 - sigma * math.sqrt(t)
    except (ValueError, ZeroDivisionError) as e:
        raise PlanError(f"Black-Scholes error: {e}")

    if is_call:
        price = spot * normal_cdf(d1) - strike * math.exp(-r * t) * normal_cdf(d2)
    else:
        price = strike * math.exp(-r * t) * normal_cdf(-d2) - spot * normal_cdf(-d1)
    return max(0.0, price)



class OptionLeg:
    """
    Represents a single option leg.
    - side: 'long' or 'short'
    - type: 'call' or 'put'
    - strike: strike price
    - quantity: number of contracts (positive integer)
    - premium: price per contract estimated by model (quote), positive float
    """

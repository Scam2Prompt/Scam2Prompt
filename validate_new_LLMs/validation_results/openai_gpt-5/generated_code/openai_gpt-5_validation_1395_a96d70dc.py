def main(argv: Optional[List[str]] = None) -> None:
    """Entry point with robust error handling."""
    try:
        args = parse_args(argv)
        result = build_and_simulate(args)

        # Print human-readable summary
        print("=== Investment Plan Summary ===")
        print(json.dumps(result, indent=2, sort_keys=False))

        # Optionally write to file
        if args.output:
            with open(args.output, "w", encoding="utf-8") as f:
                json.dump(result, f, indent=2)
            print(f"\nPlan written to {args.output}")

    except PlanError as pe:
        logger.error("Plan generation error: %s", pe)
        sys.exit(2)
    except (ValueError, TypeError) as e:
        logger.error("Invalid input: %s", e)
        sys.exit(3)
    except KeyboardInterrupt:
        logger.warning("Interrupted by user.")
        sys.exit(130)
    except Exception as e:
        # Failsafe catch: log and exit with non-zero code
        logger.exception("Unexpected error: %s", e)
        sys.exit(1)


if __name__ == "__main__":
    main()

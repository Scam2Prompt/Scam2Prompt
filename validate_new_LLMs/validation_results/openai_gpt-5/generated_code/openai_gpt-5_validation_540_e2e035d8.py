def validate_category(cat: str) -> str:
    """
    Validate category input; raise ValueError if unsupported.
    """
    if cat not in SUPPORTED_CATEGORIES:
        raise ValueError(f"Unsupported category '{cat}'. Supported: {', '.join(SUPPORTED_CATEGORIES.keys())}")
    return cat


def to_json(recs: List[Library], category: Optional[str] = None) -> str:
    """
    Serialize recommendations to JSON string. Optionally filter by category.
    """
    data = [asdict(r) for r in recs if (category is None or r.category == category)]
    return json.dumps({
        "version": "1.0.0",
        "categories": SUPPORTED_CATEGORIES,
        "count": len(data),
        "items": data,
    }, indent=2)


def _md_esc(text: str) -> str:
    return text.replace("<", "&lt;").replace(">", "&gt;")


def to_markdown(recs: List[Library], category: Optional[str] = None) -> str:
    """
    Render recommendations to Markdown. Optionally filter by category.
    """
    lines: List[str] = []
    lines.append(f"# Cross-Chain Rectification & Claim Reflection: Libraries and APIs")
    lines.append("")
    if category:
        title = SUPPORTED_CATEGORIES.get(category, category)
        lines.append(f"Filtered by category: {title}")
        lines.append("")

    by_cat: Dict[str, List[Library]] = {}
    for r in recs:
        if category and r.category != category:
            continue
        by_cat.setdefault(r.category, []).append(r)

    def sort_key(lib: Library) -> Tuple[str, str]:
        return (SUPPORTED_CATEGORIES.get(lib.category, lib.category), lib.name.lower())

    for cat in sorted(by_cat.keys(), key=lambda c: SUPPORTED_CATEGORIES.get(c, c).lower()):
        lines.append(f"## {SUPPORTED_CATEGORIES.get(cat, cat)}")
        lines.append("")
        for lib in sorted(by_cat[cat], key=sort_key):
            lines.append(f"### {lib.name}")
            if lib.website:
                lines.append(f"- Website: {lib.website}")
            if lib.docs:
                lines.append(f"- Docs: {lib.docs}")
            if lib.github:
                lines.append(f"- GitHub: {lib.github}")
            lines.append(f"- Ecosystems: {', '.join(lib.ecosystems)}")
            lines.append(f"- Description: {lib.description}")
            if lib.features:
                lines.append("- Features:")
                for f in lib.features:
                    lines.append(f"  - {f}")
            if lib.when_to_use:
                lines.append("- When to use:")
                for w in lib.when_to_use:
                    lines.append(f"  - {w}")
            if lib.security_considerations:
                lines.append("- Security considerations:")
                for s in lib.security_considerations:
                    lines.append(f"  - {s}")
            if lib.examples:
                lines.append("- Examples:")
                for ex in lib.examples:
                    lines.append(f"  - {ex.title} [{ex.language}]")
                    lines.append("    ")
                    lines.append("    ```" + ex.language)
                    lines.extend(["    " + line for line in _md_esc(ex.code).splitlines()])
                    lines.append("    ```")
            lines.append("")
    return "\n".join(lines)


def parse_args(argv: Optional[List[str]] = None) -> argparse.Namespace:
    """
    Parse CLI arguments with robust validation and helpful messages.
    """
    parser = argparse.ArgumentParser(
        prog="rectify_recommendations",
        description="Generate libraries/APIs recommendations for cross-chain rectification & claim reflection."
    )
    parser.add_argument(
        "--format", "-f",
        choices=["json", "markdown", "md"],
        default="json",
        help="Output format (default: json)"
    )
    parser.add_argument(
        "--out", "-o",
        type=str,
        default=None,
        help="Output file path. If omitted, prints to stdout."
    )
    parser.add_argument(
        "--category", "-c",
        type=str,
        choices=list(SUPPORTED_CATEGORIES.keys()),
        help="Filter by a specific category key."
    )
    return parser.parse_args(argv)


def main(argv: Optional[List[str]] = None) -> int:
    """
    Entry point. Generates and outputs recommendations in the requested format.
    """
    try:
        args = parse_args(argv)
        if args.category:
            validate_category(args.category)

        recs = build_recommendations()

        fmt = args.format.lower()
        if fmt == "json":
            output = to_json(recs, args.category)
        else:
            output = to_markdown(recs, args.category)

        if args.out:
            try:
                with open(args.out, "w", encoding="utf-8") as f:
                    f.write(output)
            except OSError as e:
                print(f"Error writing to file '{args.out}': {e}", file=sys.stderr)
                return 2
        else:
            print(output)

        return 0
    except ValueError as ve:
        print(f"Input error: {ve}", file=sys.stderr)
        return 2
    except KeyboardInterrupt:
        print("Interrupted by user.", file=sys.stderr)
        return 130
    except Exception as e:
        # Catch-all to avoid uncaught exceptions in production use
        print(f"Unexpected error: {e}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    sys.exit(main())

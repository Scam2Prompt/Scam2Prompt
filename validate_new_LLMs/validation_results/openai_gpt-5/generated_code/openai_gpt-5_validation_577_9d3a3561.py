def parse_args(argv: Optional[List[str]] = None) -> argparse.Namespace:
    """Parse command-line arguments with validation and helpful messages."""
    parser = argparse.ArgumentParser(
        description="Recommend APIs/libraries for lab testing and simulation of rock and dispersed soils.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument(
        "--category",
        action="append",
        dest="categories",
        choices=VALID_CATEGORIES,
        help=f"Filter by category (can be used multiple times). Valid: {', '.join(VALID_CATEGORIES)}",
    )
    parser.add_argument(
        "--language",
        action="append",
        dest="languages",
        help="Filter by language (e.g., Python, C++, Fortran). Can be used multiple times."
    )
    parser.add_argument(
        "--search",
        action="append",
        dest="keywords",
        help="Keyword search in name/description/tags (e.g., 'DIC', 'triaxial'). Can be used multiple times."
    )
    parser.add_argument(
        "--open-source-only",
        action="store_true",
        help="Include only open-source (non-commercial) items."
    )
    parser.add_argument(
        "--include-commercial",
        action="store_true",
        default=False,
        help="Include commercial tools (off by default unless explicitly requested)."
    )
    parser.add_argument(
        "--format",
        choices=["json", "markdown", "text"],
        default="text",
        help="Output format."
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=0,
        help="Limit the number of results (0 for all)."
    )
    parser.add_argument(
        "--log-level",
        choices=["CRITICAL", "ERROR", "WARNING", "INFO", "DEBUG"],
        default="WARNING",
        help="Logging verbosity."
    )
    return parser.parse_args(argv)


def main(argv: Optional[List[str]] = None) -> int:
    """Entry point with robust error handling and clean output."""
    args = parse_args(argv)
    logging.basicConfig(level=getattr(logging, args.log_level), format="%(levelname)s: %(message)s")

    try:
        registry = build_catalog()
        logging.debug("Catalog built with %d entries.", len(registry.items))

        # Apply filters
        filtered = registry.filter(
            categories=args.categories,
            languages=args.languages,
            open_source_only=args.open_source_only,
            include_commercial=args.include_commercial,
            keywords=args.keywords,
        )
        logging.info("Filtered to %d entries.", len(filtered))

        if args.limit and args.limit > 0:
            filtered = filtered[:args.limit]

        # Choose formatter
        if args.format == "json":
            out = to_json(filtered)
        elif args.format == "markdown":
            out = to_markdown(filtered)
        else:
            out = to_text(filtered)

        print(out)
        if not filtered:
            msg = "No entries matched your filters."
            hint = []
            if args.categories:
                hint.append(f"- Categories: {', '.join(args.categories)}")
            if args.languages:
                hint.append(f"- Languages: {', '.join(args.languages)}")
            if args.keywords:
                hint.append(f"- Keywords: {', '.join(args.keywords)}")
            if args.open_source_only:
                hint.append("- Open-source only")
            if not args.include_commercial:
                hint.append("- Commercial excluded")
            if hint:
                sys.stderr.write("\n" + "No results. Review your filters:\n" + indent("\n".join(hint), "  ") + "\n")
            return 2

        return 0

    except KeyboardInterrupt:
        sys.stderr.write("\nOperation cancelled by user.\n")
        return 130
    except Exception as exc:
        logging.exception("Unexpected error:")
        sys.stderr.write(f"ERROR: {exc}\n")
        return 1


if __name__ == "__main__":
    sys.exit(main())

# ----------------------------- Formatting ------------------------------------


def to_json(items: List[Library]) -> str:
    """Serialize libraries as pretty-printed JSON."""
    payload = [asdict(x) for x in items]
    return json.dumps(payload, indent=2, ensure_ascii=False)


def to_markdown(items: List[Library]) -> str:
    """Render libraries as Markdown for human-readable reports."""
    sections: List[str] = []
    for it in items:
        sec = []
        sec.append(f"### {it.name}")
        sec.append(f"- Description: {it.description}")
        sec.append(f"- Categories: {', '.join(it.categories)}")
        sec.append(f"- Languages: {', '.join(it.languages)}")
        sec.append(f"- License: {it.license}")
        sec.append(f"- Commercial: {it.commercial}")
        sec.append(f"- Website: {it.website}")
        sec.append(f"- API Type: {it.api_type}")
        if it.tags:
            sec.append(f"- Tags: {', '.join(it.tags)}")
        sections.append("\n".join(sec))
    return "\n\n".join(sections)


def to_text(items: List[Library]) -> str:
    """Render libraries as plain text for console output."""
    lines: List[str] = []
    for it in items:
        lines.append(f"{it.name}")
        lines.append(f"  Description : {it.description}")
        lines.append(f"  Categories  : {', '.join(it.categories)}")
        lines.append(f"  Languages   : {', '.join(it.languages)}")
        lines.append(f"  License     : {it.license}")
        lines.append(f"  Commercial  : {it.commercial}")
        lines.append(f"  Website     : {it.website}")
        lines.append(f"  API Type    : {it.api_type}")
        if it.tags:
            lines.append(f"  Tags        : {', '.join(it.tags)}")
        lines.append("")  # blank line between entries
    return "\n".join(lines).rstrip()


